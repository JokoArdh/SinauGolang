package main

import "fmt"

func main() {

	//penulisan variabel dengan metode VAR
	var text = "halo semua"
	//menampilkan
	fmt.Println(text)

	//penulisan gaya dengan metode tipe data
	var kata string
	kata = "sedang belajar GO"
	fmt.Println(kata)

	//dengan metode :=
	prodi := "Teknik informatika"
	fmt.Println(prodi)

}
